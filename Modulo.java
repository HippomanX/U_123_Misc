public class Modulo {
   public static boolean isEven(int i){
   if (i%2==0) return true;
   else return false;
   /* checks if an integer is even
      Inputs:
         int i: the integer we are checking
      Outputs:
         return: true if i is even, false otherwise (0 is even)
   */
}
   public static boolean isOdd(int i){
   if (i%2==0) return false;
   else return true;
   /* checks if an integer is odd
      Inputs:
         int i: the integer we are checking
      Outputs:
         return: true if i is odd, false otherwise (0 is not odd)
   */
}

}